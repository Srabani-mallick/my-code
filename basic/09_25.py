import math
# Square root
sqrt_value = math.sqrt(16)
# Power
pow_value = math.pow(2, 3)
# Factorial
factorial_value = math.factorial(5)
# Ceiling
ceil_value = math.ceil(4.2)
# Floor
floor_value = math.floor(4.8)
# Absolute value
fabs_value = math.fabs(-10.5)
# Output results
print("Square root of 16:", sqrt_value)
print("2 raised to the power of 3:", pow_value)
print("Factorial of 5:", factorial_value)
print("Ceiling of 4.2:", ceil_value)
print("Floor of 4.8:", floor_value)
print("Absolute value of -10.5:", fabs_value)